# 문제
# 두 개의 정수를 입력 받아 다음과 같이 출력하는 프로그램을 작성하시오. 

# 예제
# 수를 입력하시오. 5
# 수를 입력하시오. 2
# 5 + 2 = 7
# 5 - 2 = 3
# 5 * 2 = 10
# 5 // 2 = 2
# 5 % 2 = 1
# 5 ** 2 = 25

# 풀이
a = input("수를 입력하시오.")
b = input("수를 입력하시오.")
a = int(a)
b = int(b)

print(a,"+",b,"=",a + b)
print(a,"-",b,"=",a - b)
print(a,"*",b,"=",a * b)
print(a,"//",b,"=",a // b)
print(a,"%",b,"=",a % b)
print(a,"**",b,"=",a ** b)
